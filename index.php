					                 <h1> THE ART OF GAINING WEIGHT</h1>
                              <div class="container">
                                <h2 id="C1">The Basis of Weight Gain</h2>
                                <img src="58ebd87e55adae4f152e3f6a9837e878.jpg"></img>
                                <text><br>
                                  The most important thing you can do to gain weight is to eat more calories than your body needs.
                                  A caloric surplus (calories in > calories out) is needed. Without it, you won't gain. Period. If
                                  you want to gain weight slowly and steadily, then aim for 300-500 calories more than you burn. If
                                   you want to gain weight fast, then aim for something like 700-1000 calories above your maintenance
                                    level. Keep in mind that calorie calculators only provide estimates. Your needs may vary by several
                                     hundred calories per day, give or take. You don't need to count calories for the rest of your life,
                                      but it helps to do it for the first few days/weeks to get a feel for how many calories you are eating.
                                       I recommend using tools like Cron-o-meter or MyFitnessPal to track your intake.<br><br><br><br>
                                </text>
                              </div>

                              <div class="container">
                                <h2 id="C2">The Process of Gaining Weight</h2>
                                <img class= "small-pcs" src="http://i.imgur.com/9DbuWn1.gif"></img>
                                <text><br>
                                  Your body is a machine that constantly reinvents itself. Every minute of every day, it breaks down
                                   its own tissues and replaces them with new stuff it makes from a combination of the food you eat and
                                    recycled material it scavenges from other tissues. Workouts will cause the protein in your muscles
                                    to break down and build up much faster than it does in ordinary circumstances. The entire point of
                                    a workout is to get that protein to turn over. But it only helps if you end up with more than you had
                                     when you started. There are two ways to do that. The first, and by far the easiest, is to eat more
                                     protein than you currently do. Protein, all by itself, is anabolic. It wants to be stored in your
                                     muscles. The second is to work out in a way that disrupts your muscles and forces them to respond
                                     by getting bigger and stronger.The combination of a diet rich in high-quality protein and a great
                                     strength-training program is the oldest, best, and only non-pharmaceutical way to reach that goal.<br><br><br><br>
                                </text>
                              </div>

                              <div class="container">
                                <h2 id="C3">What To Eat To Gain Weight Not Fat</h2>
                                <img src="BEST-BODYBUILDING-DIET-PLAN-TO-START.png"></img>
                                <text ><br>
                                   Building muscle requires an increase in calories; that is, to gain weight you must eat more
                                   calories than you burn each day. But if you go overboard and eat too much, you'll kick-start the
                                    fat-storing process. So the key is to eat just enough to facilitate the muscle-gaining process
                                    but not so much that you'll add fat along with it. One way to do this is by controlling portion
                                     sizes at mealtime. For most meals (not including post workout), aim to get 40-60 grams of protein
                                     and 40-80 grams of carbs, depending on your size; bigger guys weighing more than, say, 225 pounds
                                      will shoot for the higher end. Dietary fat should be as low as possible, except for healthy fats
                                      (from nuts, olive oil, fatty fish), which can amount to 5-10 grams per meal.<br><br><br><br>
                                 </text>
                              </div>

                              <div class="container">
                                <h2 id="C4">No, Carbs Are Good For You</h2>
                                <img src="p01cgx0l.jpg"></img>
                                <text ><br>
                                  Three cups of rice, pasta or even a couple of bagels at a single sitting? Sounds like
                                  it'd make you fat, right? Not if you consume it along with lean protein immediately following
                                   a training session. Carbs remain the mismanaged nutrient. While they have the ability to be
                                    stored as bodyfat, they're crucial to the muscle-building process. When you eat a lot of
                                    carbohydrates after training, it sets off a cascade of hormonal changes that favor the rebuilding
                                     of muscle mass. This includes a rise in insulin, which not only forces protein into muscles for
                                      growth but also stabilizes testosterone levels, which often fall as a result of too few carbs
                                      after training. On the flip side, if you eat too many carbs and just sit around being fairly
                                      inactive, some of those carbs might end up as bodyfat. That's why you should eat fewer carbs
                                      on days you don't train. While you need them to grow on days you work out, your need for them
                                      goes down considerably on days when you don't workout.<br><br><br><br>
                                </text>
                              </div>

                              <div class="container">
                                <h2 id="C5">When To Eat For Proper Weight Gain</h2>
                                <img src="download (1).jpg"></img><img src="download.jpg"></img>
                                <text ><br>
                                Meal timing is the other key to staying lean while bulking up. When you eat not only supports
                                mass gains but also plays a pivotal role in controlling body fat levels. If you're trying to gain
                                 only quality mass, increase the size of your meals at breakfast and after training. These are the
                                  two times of day when muscles crave more calories and nutrients at breakfast because you're
                                  nutritionally depleted after a night's sleep, and post-workout because the stressed muscles
                                  are in dire need of replenishment to jump-start the recovery process. Providing the body with
                                  what it can put to use during these windows facilitates optimum growth and keeps bodyfat levels
                                  down. In short, smart growth - muscle sans bodyfat - is contingent on manipulating calorie intake.
                                  Yes, you have to eat more to gain mass, but when you eat more can determine whether you'll gain
                                  fat or muscle. If you stick to a large breakfast and a substantial post-training meal and evenly
                                  divide your other meals into smaller portions, you can boost your total caloric intake, ensuring
                                  that those extra calories go to the muscles when they need them.<br><br><br><br>
                                </text>
